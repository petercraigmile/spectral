
## ======================================================================
## Purpose : convert 'x' to decibels
## Updated : pfc@stat.osu.edu, Dec 2002.
## ======================================================================

decibels <- dB <- function (x)
{
  10 * log10(x)
}


## ======================================================================
## Purpose : convert 'x' from decibels
## Updated : pfc@stat.osu.edu, Dec 2002.
## ======================================================================


idecibels <- idB <- function (x)
{
  10^(x/10)
}
